let count = 0;
const updated = terms.map(term => {
  if (term[6]) return term;
  const detailed = detailedDefs[term[0]];
  if (detailed) {
    count++;
    return [...term, detailed];
  }
  return term;
});

fs.writeFileSync('terms.json', JSON.stringify(updated, null, 2), 'utf-8');

const cats = {};
updated.forEach(r => {
  const c = r[3];
  if (!cats[c]) cats[c] = { total: 0, detailed: 0 };
  cats[c].total++;
  if (r[6]) cats[c].detailed++;
});
console.log(`本次新增 ${count} 条，总计 ${Object.values(cats).reduce((s,v)=>s+v.detailed,0)} 条详细释义`);
