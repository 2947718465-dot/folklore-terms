function expandDetailed(term) {
  const [cn, en, def, cat, sub, t3, oldDetailed] = term;
  if (!oldDetailed) return null;
  if (oldDetailed.length >= 100) return null;

  const template = subTemplates[sub] || '';
  if (!template) return null;

  if (oldDetailed.includes(template.substring(0, 20))) return null;

  return oldDetailed + '\n' + template;
}

let count = 0;
const updated = terms.map(term => {
  if (!term[6]) return term;
  const expanded = expandDetailed(term);
  if (expanded && expanded !== term[6]) {
    count++;
    return [...term.slice(0, 6), expanded];
  }
  return term;
});

fs.writeFileSync('terms.json', JSON.stringify(updated, null, 2), 'utf-8');

let short = 0, medium = 0, long = 0;
updated.forEach(r => {
  if (!r[6]) return;
  const len = r[6].length;
  if (len < 50) short++;
  else if (len < 100) medium++;
  else long++;
});
console.log(`本次扩充 ${count} 条`);
console.log(`扩充后: <50字:${short}条, 50-100字:${medium}条, 100-300字:${long}条`);
