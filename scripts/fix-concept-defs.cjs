let count = 0;
const updated = terms.map(term => {
  if (term[3] !== '学科概念') return term;
  const newDef = correctedDefs[term[0]];
  if (newDef && newDef !== term[2]) {
    count++;
    return [term[0], term[1], newDef, term[3], term[4], term[5], term[6]];
  }
  return term;
});

fs.writeFileSync('dist/terms.json', JSON.stringify(updated));
fs.writeFileSync('public/terms.json', JSON.stringify(updated));

// 同步更新 terms-detailed.json
const detailed = {};
updated.forEach((r, i) => {
  if (r[6]) detailed[i] = r[6];
});
fs.writeFileSync('dist/terms-detailed.json', JSON.stringify(detailed));
fs.writeFileSync('public/terms-detailed.json', JSON.stringify(detailed));

console.log(`修正了 ${count} 条学科概念的简要释义`);

// 验证
let stillGeneric = 0;
updated.filter(r => r[3] === '学科概念').forEach(r => {
  const def = r[2] || '';
  if (def === '民众创造享用和传承的生活文化和风俗习惯' ||
      def.includes('共享特定文化传统情感纽带或社会认同的群体') ||
      def.includes('社会文化现象在整体系统中发挥的作用和效能') ||
      def.includes('具有共同文化特征和历史记忆')) {
    stillGeneric++;
  }
});
console.log(`学科概念中仍使用通用释义: ${stillGeneric} 条`);
